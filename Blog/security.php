    <div class="intro-paragraph-text font-caslon">
    <h2 class="heading cms-heading font-caslon">Security Issues reveal by Google regarding Artificial Intelligence</h2>
    <p>In a recent article published by Google, they’ve reveled five major security problems related to Artificial Intelligence. From now on, companies will have to fallow a guide on their future Al system in order to control robots before they can actually interact with humans. </p>
 <p>The artificial intelligence is designed to mimic the human brain, or at least its logic when it comes to making decisions. Before worrying about whether an artificial intelligence (AI) could become so powerful that can dominate humans, it would be better to make sure that robots (also called our future colleagues and household companions) are trustworthy. That’s what Google have tried to explain us. Google’s artificial intelligence specialists have worked with researchers from the Universities of Stanford and Berkeley (California, USA) and with the Association OpenAI on concrete security issues that we must work to resolve.
  </p>
 <p>In white paper titled “Concrete Problems in AI Safety” this team describes five “practical problems” of accidents in artificial intelligence based machine could cause if they aren’t designed properly. Al specialists define accidents as “unexpected and harmful behavior that may emerge from poor design of real world machine learning systems”. In short, these are not potential errors of robots we should be feared but those of their designers. </p>
 <p>
To concretely illustrate their point of view, the authors of this study voluntarily took a random example of a “cleaning robot”. However, it’s quite clear that the issues apply to all forms of AI controlling a robot.
</p>
