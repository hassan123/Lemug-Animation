    <h2 class="heading cms-heading font-caslon">  A robot may disrupt the environment</h2>
    <p>The first two risks identified by the researchers from Google and their acolytes are related to a poor coordination and allocation of the main objective. There is first what they call “Avoiding Negative Side Effects”. Specifically, how to avoid environment related problems caused by a robot while it’s accomplishing its mission. For example, the cleaner could well topple or crush what is on his way because he calculated the fastest route to complete its task. To prevent this scenario, the solution may be to create “constraints of common sense” in the form of penalties imposed on the IA when he causes a major disruption to the environment in which the robot moves.

