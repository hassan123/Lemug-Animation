    <p>Second risk of Al based machines is to avoiding reward hacking. For IA, the reward is the success of the goal. Avoid the quest reward from turning into a game and the machine trying to get by all means, even skip steps or cheat. In the case of cleaning robot, it would for example to hide the dirt under the rug in order to say “that’s it, I’m done.”
A difficult problem to solve as an Al can be interpreted in many different ways a task and the environment it meets. One of the ideas in the article is to truncate the information so that the program does not have a perfect knowledge of how to get a reward and thus does not seek to go shorter or easier.
