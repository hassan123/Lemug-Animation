                <span class="subtitle-separator">/</span>
            </h2>
           
                    <p>
                        Wikipedia is such a well-known encyclopedia, which counts on users to add their own knowledge to the mix. Wikipedia has pretty specific guidelines laid out. The purpose of Wikipedia approval mechanism is, essentially, quality assurance. By presenting particular articles as approved, we would be representing those articles as reliable sources of information. Large, reputable websites and the web, in general, are more likely to use or link to Wikipedia content if it has been approved by experts. Maybe one of the three types resonates with you: </p>
<p>1. Has your business invented an industry-leading, technology changing or notable invention? </p>
<p>2. If the company founder or the business itself stands out for another reason, or your competitor is featured on Wikipedia, then I would recommend that you will be mentioned on Wikipedia too. </p>
<p>3. Is your entry going to be verifiable? For example, are there references to the internet that you can link to a press release or something similar? </p>
<p>4. You are unable to find anything related to your technology/products on Wikipedia and therefore there is a gap between “knowledge” on the site.</p>
<p>If your answer is “no” to the information you’d like to submit to the following attributes, then don’t bother with creating the entry, as it will simply be deleted and your hard work was for nothing: </p>
<p>•    Worthwhile </p>
<p>•    Notable </p>
<p>•    Verifiable </p>
